// var
var a = 10; // declare and initialised
a = true // we can re initialisation
var a = "Hello" // we can also re declare
console.log("The value of a is "+a);

// let
let b = 10; // declare and initialised
b = true // we can re initialisation
// let b = "Hello" we can not re declare
console.log("The value of a is "+b);

// constant
const c = 10; // declare and initialised
// c = true we can not re initialisation as well
// const c = "Hello" we can not re declare
console.log("The value of a is "+c);

