let arr1 = [18, "hi", true, null, 76];
console.log(arr1);
console.log(arr1.length);

// array inbuilt method
// push
arr1.push("Hello", true);
console.log(arr1);

//pop
arr1.pop();
arr1.pop();
console.log(arr1);

//unshift

arr1.unshift("rohit", "singh");
console.log(arr1);

//shift

arr1.shift();
arr1.shift();
console.log(arr1);

// splice

arr1.splice(4, 0, "rohit", "singh");
console.log(arr1);

